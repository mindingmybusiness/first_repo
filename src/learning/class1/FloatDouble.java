package learning.class1;

public class FloatDouble {

	public static void main(String[] args) {
		float myMinFloatValue = Float.MIN_VALUE;
		float myMaxFloatValue = Float.MAX_VALUE;
		System.out.println("min value is: "+ myMinFloatValue);
		System.out.println("max value is:"+ myMaxFloatValue);
		
		double myMinDoubleValue = Double.MIN_VALUE;
		double myMaxDoubleValue = Double.MAX_VALUE;
		System.out.println("min value is: "+ myMinDoubleValue);
		System.out.println("Max value is: "+ myMaxDoubleValue);
		
		int myVal = 5;
		float myVal2 = 5;
		double myVal3 = 5;
		
		System.out.println(myVal);
		System.out.println(myVal2);
		System.out.println(myVal3);
	}

}
